#pragma once
#include "DataNode.h"

template<class Key, class T> class BinaryTree {
private:

	struct TreeNode : public DataNode<T> {
		TreeNode(Key* key,T* value);
		Key* key;
		TreeNode* parentBranch;
		TreeNode* leftBranch;
		TreeNode* rightBranch;
		int childrenCount = 0;

		void updateCount();
	};

	
	TreeNode* root;


public:
	BinaryTree();
	void Add(Key* key, T* value);
};

